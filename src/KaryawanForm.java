    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            databaru = false;
            
            int row = jTable1.getSelectedRow();
            String id_karyawan = jTable1.getModel().getValueAt(row, 0).toString();
            
            Connection conn = koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM crud_java WHERE id='" + id_karyawan+ "'");
            
            if(sql.next()) {
                jTextField1.setText(sql.getString("id"));
                jTextField2.setText(sql.getString("nama"));
                jTextField3.setText(sql.getString("nik"));
                jTextField4.setText(sql.getString("jabatan"));
                jTextField5.setText(sql.getString("no_hp"));
                jTextField6.setText(sql.getString("alamat"));
            }
        } catch (SQLException ex) {
                Logger.getLogger(KaryawanForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }  
    }//GEN-LAST:event_jTable1MouseClicked

